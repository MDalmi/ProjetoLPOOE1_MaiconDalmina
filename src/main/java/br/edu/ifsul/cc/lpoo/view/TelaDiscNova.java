    public void listarProf() {
        cmbProfessores.removeAllItems();
        PersistenciaJPA jpa = new PersistenciaJPA();
        jpa.conexaoAberta();
        List<Professores> lista = jpa.getProfessores();

        for (Professores o : lista) {
            cmbProfessores.addItem(o);
        }

        jpa.fecharConexao();
    }


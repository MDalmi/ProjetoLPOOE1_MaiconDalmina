    private void btnNovaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovaActionPerformed
        String nome = txtNome.getText();
        PersistenciaJPA jpa = new PersistenciaJPA();
        Disciplinas disc = new Disciplinas();
          
            if (nome.length() != 0)
                {
                    try {       
                        Professores p1 = (Professores) cmbProfessores.getSelectedItem();
                        disc.setNomeDisciplina(nome);
                        disc.setProfessor(p1);
                        jpa.conexaoAberta();
                        jpa.persist(disc);       
                        JOptionPane.showMessageDialog(rootPane, "Disciplina Nova!");
                    } catch (Exception e) {
                        System.err.println("Erro ao adicionar Disciplina: " + e.getMessage());
                    } finally {
                        jpa.fecharConexao();
                        dispose();
                }
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Nome do aluno deve ser preenchido!");
    }//GEN-LAST:event_btnNovaActionPerformed

